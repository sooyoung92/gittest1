package ch01;

public class DataType3 {
	//메인 함수 ( 코드의 시작점 )
	public static void main(String[] args) {
		
		//기본 자료형 - 실수형
		
		//float, double
		float name = 0.5F; //접미사를 사용 (F or f)
		
		// 8바이트 -> 소수점을 많이 표시 할 수 있기 때문에
		// 정확성을 더 보장한다.
		double name2 = 0.123;
		
		System.out.println(name);
		System.out.println(name2);
		
		
	}// end of main

}// end of class
